			<p>Second Frequency nació en el año 2015, en Córdoba, Argentina, fundada por Martín Ferreyra Lucía González y Santiago Ríos, músicos aficionados que habían notado una falta de opciones accesibles y especializadas en instrumentos musicales en la región. </p>

			<p>Desde sus inicios, la empresa se enfocó no solo en la venta de instrumentos nuevos, sino también en la compra y venta de productos musicales de segunda mano, una propuesta que rápidamente captó la atención de artistas independientes, estudiantes y profesionales que buscaban calidad a precios accesibles.</p>

			<p>En 2018, con el negocio ya consolidado y una creciente comunidad de clientes se integraron en la empresa servicios de reparación, asesoramiento técnico y una plataforma online para extender su alcance a todo el país.</p>
